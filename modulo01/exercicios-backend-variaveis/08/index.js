//Calculo do perímetro e área de um círculo

let raioCirculo = 1
const valorPi = 3.14;
calculoComprimento = (2 * valorPi * raioCirculo);
calculoArea = (valorPi * (raioCirculo ** 2));

console.log(`O comprimento do circulo é ${calculoComprimento}`);
console.log(`A área do circulo é ${calculoArea}`);
